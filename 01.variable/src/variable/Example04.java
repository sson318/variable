package variable;

public class Example04 {

	public static void main(String[] args) {
		//문자열
		//자바에서 문자열은 쌍따움표 사이에 감싸서 사용해야 한다.
		//공백, 띄어쓰기, 한단어도 전부 문자열로 취급할 수 있다.
		
		String s;
		s = "hello";
		System.out.println(s);
		
		s = "h";
		System.out.println(s);
		
		s = "";
				System.out.println(s);
				
		s = " ";
		System.out.println(s);
		
		System.out.println("----------------");

	}

}
